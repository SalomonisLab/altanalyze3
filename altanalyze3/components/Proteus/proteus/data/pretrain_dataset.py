"""
SyntheticExonSkipDataset: Dataset for self-supervised pretraining.

Loads synthetic exon-skip pairs generated by SyntheticExonSkipGenerator
and their cached embeddings.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any, Dict, List, Optional

import numpy as np
import torch
from torch.utils.data import Dataset


class SyntheticExonSkipDataset(Dataset):
    """
    Dataset for Proteus self-supervised pretraining on synthetic exon-skip pairs.

    Reads from data/interim/pretrain_events/synthetic_exon_skips.tsv and
    loads pre-cached embeddings for each (reference, skip-variant) pair.

    TSV columns expected:
        reference_transcript_id, alternative_transcript_id, reference_protein_id,
        domain_disrupted (0/1), nmd_triggered (0/1), tm_lost (0/1),
        disorder_delta (float), frame_preserved (0/1)

    Parameters
    ----------
    tsv_path : Path
        Path to synthetic_exon_skips.tsv.
    cache_dir : Path
        Root cache directory with rna_embeddings/, protein_embeddings/, etc.
    max_samples : int | None
        If set, limit dataset to this many rows (for debugging).
    """

    RNA_DIM = 512
    PROTEIN_DIM = 1280
    DISORDER_DIM = 50
    STRUCTURAL_DIM = 48

    def __init__(
        self,
        tsv_path: Path,
        cache_dir: Path,
        max_samples: Optional[int] = None,
    ) -> None:
        import pandas as pd

        self.cache_dir = Path(cache_dir)
        self.rna_cache = self.cache_dir / "rna_embeddings"
        self.protein_cache = self.cache_dir / "protein_embeddings"
        self.disorder_cache = self.cache_dir / "disorder_features"
        self.structural_cache = self.cache_dir / "structural_features"

        df = pd.read_csv(tsv_path, sep="\t", low_memory=False)

        if max_samples is not None:
            df = df.head(max_samples)

        self.records = df.to_dict("records")

        print(
            f"[SyntheticExonSkipDataset] Loaded {len(self.records)} synthetic pairs "
            f"from {tsv_path}"
        )

    @staticmethod
    def _safe_float(val, default: float = 0.0) -> float:
        try:
            if val is None or (isinstance(val, float) and np.isnan(val)):
                return default
            return float(val)
        except (ValueError, TypeError):
            return default

    @staticmethod
    def _safe_bool_label(val) -> int:
        """Return 0 or 1 from a boolean-like column."""
        try:
            return int(float(val))
        except (ValueError, TypeError):
            return 0

    def _load_rna(self, transcript_id: str) -> torch.Tensor:
        if not transcript_id or transcript_id.lower() in ("nan", "none", ""):
            return torch.zeros(self.RNA_DIM)
        path = self.rna_cache / f"{transcript_id}.pt"
        if path.exists():
            return torch.load(path, weights_only=True)
        return torch.zeros(self.RNA_DIM)

    def _load_protein(self, protein_id: str) -> tuple[torch.Tensor, bool]:
        if not protein_id or protein_id.lower() in ("nan", "none", ""):
            return torch.zeros(self.PROTEIN_DIM), False
        pt_path = self.protein_cache / f"{protein_id}.pt"
        absent_path = self.protein_cache / f"{protein_id}.absent"
        if absent_path.exists():
            return torch.zeros(self.PROTEIN_DIM), False
        if pt_path.exists():
            return torch.load(pt_path, weights_only=True), True
        return torch.zeros(self.PROTEIN_DIM), True

    def _load_disorder(self, protein_id: str) -> torch.Tensor:
        if not protein_id or protein_id.lower() in ("nan", "none", ""):
            return torch.zeros(self.DISORDER_DIM)
        path = self.disorder_cache / f"{protein_id}.npy"
        if path.exists():
            arr = np.load(str(path)).astype(np.float32)
            return torch.from_numpy(arr)
        return torch.zeros(self.DISORDER_DIM)

    def _load_structural(self, protein_id: str) -> torch.Tensor:
        if not protein_id or protein_id.lower() in ("nan", "none", ""):
            return torch.zeros(self.STRUCTURAL_DIM)
        path = self.structural_cache / f"{protein_id}.pt"
        if path.exists():
            t = torch.load(path, weights_only=True)
            return t.float() if isinstance(t, torch.Tensor) else torch.from_numpy(t).float()
        return torch.zeros(self.STRUCTURAL_DIM)

    def __len__(self) -> int:
        return len(self.records)

    def __getitem__(self, idx: int) -> Dict[str, Any]:
        """
        Load embeddings and SSL labels for one synthetic exon-skip pair.

        Returns
        -------
        dict
            Embedding fields + SSL labels: domain_disrupted, nmd_triggered,
            tm_lost (all int 0/1), disorder_delta (float).
        """
        row = self.records[idx]

        ref_tid = str(row.get("reference_transcript_id", ""))
        alt_tid = str(row.get("alternative_transcript_id", ""))
        ref_pid = str(row.get("reference_protein_id", ""))

        # For synthetic skips, we don't have alt protein — use zeros
        # (the skip creates a modified protein, but we don't have its embedding)
        # NMD-triggered skips have has_alt_protein=False
        nmd = self._safe_bool_label(row.get("nmd_triggered", 0))
        has_alt_protein = not bool(nmd)

        ref_rna = self._load_rna(ref_tid)
        alt_rna = self._load_rna(alt_tid)

        ref_prot, _ = self._load_protein(ref_pid)
        # Synthetic alt proteins — use zeros (not yet embedded)
        alt_prot = torch.zeros(self.PROTEIN_DIM)

        ref_dis = self._load_disorder(ref_pid)
        alt_dis = torch.zeros(self.DISORDER_DIM)  # synthetic, use zeros

        ref_str = self._load_structural(ref_pid)
        alt_str = torch.zeros(self.STRUCTURAL_DIM)  # synthetic

        return {
            "ref_rna_emb": ref_rna,
            "alt_rna_emb": alt_rna,
            "ref_protein_emb": ref_prot,
            "alt_protein_emb": alt_prot,
            "ref_disorder_raw": ref_dis,
            "alt_disorder_raw": alt_dis,
            "ref_structural_raw": ref_str,
            "alt_structural_raw": alt_str,
            "has_alt_protein": torch.tensor(has_alt_protein, dtype=torch.bool),
            # SSL labels
            "domain_disrupted": torch.tensor(
                self._safe_bool_label(row.get("domain_disrupted", 0)), dtype=torch.float32
            ),
            "nmd_triggered": torch.tensor(
                self._safe_bool_label(row.get("nmd_triggered", 0)), dtype=torch.float32
            ),
            "tm_lost": torch.tensor(
                self._safe_bool_label(row.get("tm_lost", 0)), dtype=torch.float32
            ),
            "disorder_delta": torch.tensor(
                self._safe_float(row.get("disorder_delta", 0.0)), dtype=torch.float32
            ),
            "frame_preserved": torch.tensor(
                self._safe_bool_label(row.get("frame_preserved", 1)), dtype=torch.bool
            ),
            "reference_transcript_id": ref_tid,
            "alt_transcript_id": alt_tid,
        }
